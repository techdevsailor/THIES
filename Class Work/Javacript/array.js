//Array Methods.............

// 1. Push (add element at last)...............
const x = [1, 2, 3];
x.push(4, 5);      
// console.log(x)

//2. pop (remove last element)..................
let fruits = ['apple', 'banana', 'orange'];
fruits.pop(); 
// console.log(fruits)

//3. shift (Remove 1st element)..................

let fruit= ['apple', 'banana', 'orange'];
fruit.shift(); 
// console.log(fruit)

//4. unshift (add element at beginning)..................
let z= ['apple', 'banana', 'orange'];
z.unshift('Strawberry'); 
// console.log(z)

//5. concat (combine/ merge 2 arrays)
let Group1 = ['apple', 'banana'];
let Group2 = ['carrot', 'broccoli'];
let Final = Group1.concat(Group2);
console.log(Final)







